// JWT 验证中间件
const authenticateToken = (req, res, next) => {
    try {
        console.log('=== JWT 验证中间件调试信息 ===')
        console.log('请求头信息:', {
            'user-agent': req.headers['user-agent'],
            cookie: req.headers.cookie,
            authorization: req.headers.authorization,
            token: req.headers.token, // 添加token header的日志
        })
        console.log('req.cookies:', req.cookies)
        console.log('req.session:', req.session)

        // 从多个地方尝试获取 token
        let token = req.cookies?.token

        // 如果 cookie 中没有，尝试从直接的 token header 获取
        if (!token && req.headers.token) {
            token = req.headers.token
        }

        // 如果还是没有，尝试从 Authorization header 获取
        if (!token && req.headers.authorization) {
            const authHeader = req.headers.authorization
            if (authHeader.startsWith('Bearer ')) {
                token = authHeader.replace('Bearer ', '')
            }
        }

        // 如果还是没有，尝试从 session 获取
        if (
            !token &&
            req.session &&
            req.session.user &&
            req.session.user.token
        ) {
            token = req.session.user.token
        }

        console.log('Token获取结果:', {
            cookie: req.cookies?.token
                ? `${req.cookies.token.substring(0, 20)}...`
                : null,
            headerToken: req.headers.token
                ? `${req.headers.token.substring(0, 20)}...`
                : null,
            authorization: req.headers.authorization,
            sessionToken: req.session?.user?.token
                ? `${req.session.user.token.substring(0, 20)}...`
                : null,
            finalToken: token ? `${token.substring(0, 20)}...` : null,
        })

        if (!token) {
            console.log('❌ 未找到有效的token')
            return res.status(401).json({ code: -1, message: '未提供访问令牌' })
        }

        jwt.verify(token, process.env.JWT_SECRET, (err, user) => {
            if (err) {
                console.error('❌ JWT 验证失败:', err.message)
                return res
                    .status(403)
                    .json({ code: -1, message: '令牌无效或已过期' })
            }
            req.user = user
            console.log('✅ JWT 验证成功:', user)
            next()
        })
    } catch (error) {
        console.error('❌ Token verification error:', error)
        return res.status(500).json({ code: 500, message: '服务器内部错误' })
    }
}

